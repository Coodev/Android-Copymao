public class CategoryItem {

    private int imgId;
    private String categoryItemTitle;
    private String categoryItemDetails;

    public int getimgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getCategoryItemTitle() {
        return categoryItemTitle;
    }

    public void setCategoryItemTitle(String categoryItemTitle) {
        this.categoryItemTitle = categoryItemTitle;
    }

    public String getCategoryItemDetails() {
        return categoryItemDetails;
    }

    public void setCategoryItemDetails(String categoryItemDetails) {
        this.categoryItemDetails = categoryItemDetails;
    }

}
